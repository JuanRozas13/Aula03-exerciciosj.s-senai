var raio = parseFloat(prompt("Digite o valor do raio"));

var altura = parseFloat(prompt("Digite o valor da altura"));

var pi = 3.14;

raio2 = Math.pow(raio , 2);

volume = pi * raio2 *altura;

alert("A altura é " + altura)

alert("O valor do raio é " + raio2 +"cm")

alert("O volume da lata de oléo total é de " + volume + "cm³")
